number_1 = int(input("enter your number....."))
number_2 = int(input("enter your number....."))
z = str(input("+ , - , / , *"))

if z == "+":
    print(number_1+number_2)
if z == "-":
    print(number_1-number_2)

if z == "*":
    print(number_1*number_2)
if z == "/":
    print(number_1/number_2)